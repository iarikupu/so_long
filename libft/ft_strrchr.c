#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	slen;

	slen = ft_strlen(s);
	while (slen != 0 && *(s + slen) != (char)c)
		slen--;
	if (*(s + slen) == (char)c)
		return ((char *)(s + slen));
	return (0);
}
